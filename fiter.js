// Write a function that takes an array of numbers and returns a new array where each number is doubled using map().
let numbers = [1, 2, 3, 4, 5];
let doubled = numbers.map(num => num * 2);
console.log(doubled); // [2, 4, 6, 8, 10]
 
// Given an array of lowercase strings, return a new array with all strings converted to uppercase using map().
let lower = ['apple', 'bannana', 'cherry'];
let upper = lower.map(str => str.toUpperCase());
console.log(upper); // ['APPLE', 'BANNANA', 'CHERRY']
 
